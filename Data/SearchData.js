// Helper function to search users by query
export const searchUsers = (query) => {
  if (!query || query.trim().length === 0) {
    return [];
  }

  const searchTerm = query.toLowerCase().trim();
  
  return searchableUsers.filter(user => 
    user.firstName.toLowerCase().includes(searchTerm) ||
    user.lastName.toLowerCase().includes(searchTerm) ||
    user.bio.toLowerCase().includes(searchTerm) ||
    user.location.toLowerCase().includes(searchTerm) ||
    (user.userType === 'provider' && user.category && user.category.toLowerCase().includes(searchTerm))
  );
};

// Helper function to get users by type
export const getUsersByType = (userType) => {
  return searchableUsers.filter(user => user.userType === userType);
};



// Helper function to get providers by category
export const getProvidersByCategory = (category) => {
  return searchableUsers.filter(user => 
    user.userType === 'provider' && user.category === category
  );
};

// Helper function to get available providers
export const getAvailableProviders = () => {
  return searchableUsers.filter(user => 
    user.userType === 'provider' && user.available
  );
};
